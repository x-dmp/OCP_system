package cn.edu.cuit.model.bo;

public class TransferVo {
    private String transferVoAddress;
    private String transferVoConsignee;
    private String consigneeVoTele;
    private String consigneeVoId;

    public String getTransferVoAddress() {
        return transferVoAddress;
    }

    public void setTransferVoAddress(String transferVoAddress) {
        this.transferVoAddress = transferVoAddress;
    }

    public String getTransferVoConsignee() {
        return transferVoConsignee;
    }

    public void setTransferVoConsignee(String transferVoConsignee) {
        this.transferVoConsignee = transferVoConsignee;
    }

    public String getConsigneeVoTele() {
        return consigneeVoTele;
    }

    public void setConsigneeVoTele(String consigneeVoTele) {
        this.consigneeVoTele = consigneeVoTele;
    }

    public String getConsigneeVoId() {
        return consigneeVoId;
    }

    public void setConsigneeVoId(String consigneeVoId) {
        this.consigneeVoId = consigneeVoId;
    }
}
