package cn.edu.cuit.model.entity;

import java.util.Date;

/**
 * @auther yincaiTA
 * @date 2021/1/1 11:23
 * @description 数据库日志表实体类
 */
public class Syslog {

    // 主键id
    private Long id;

    // 经销商账号的username
    private String accountName;

    // 访问controller的时间
    private Date visitTime;

    // 访问ip
    private String ip;

    // 接口path
    private String url;

    // 访问总时间
    private Integer executeTime;

    // 访问的哪个接口的方法
    private String method;

    @Override
    public String toString() {
        return "Syslog{" +
                "id=" + id +
                ", accountName='" + accountName + '\'' +
                ", visitTime=" + visitTime +
                ", ip='" + ip + '\'' +
                ", url='" + url + '\'' +
                ", executeTime=" + executeTime +
                ", method='" + method + '\'' +
                '}';
    }

    public Long getId() {
        return id;
    }

    public Syslog setId(Long id) {
        this.id = id;
        return this;
    }

    public String getAccountName() {
        return accountName;
    }

    public Syslog setAccountName(String accountName) {
        this.accountName = accountName;
        return this;
    }

    public Date getVisitTime() {
        return visitTime;
    }

    public Syslog setVisitTime(Date visitTime) {
        this.visitTime = visitTime;
        return this;
    }

    public String getIp() {
        return ip;
    }

    public Syslog setIp(String ip) {
        this.ip = ip;
        return this;
    }

    public String getUrl() {
        return url;
    }

    public Syslog setUrl(String url) {
        this.url = url;
        return this;
    }

    public Integer getExecuteTime() {
        return executeTime;
    }

    public Syslog setExecuteTime(Integer executeTime) {
        this.executeTime = executeTime;
        return this;
    }

    public String getMethod() {
        return method;
    }

    public Syslog setMethod(String method) {
        this.method = method;
        return this;
    }
}
